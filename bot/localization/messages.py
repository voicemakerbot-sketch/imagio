def normalize_language(language_code: str | None) -> str:
    if language_code and language_code.lower() in SUPPORTED_LANGUAGES:
        return language_code.lower()
    return settings.bot_default_language if settings.bot_default_language in SUPPORTED_LANGUAGES else "uk"


def get_message(key: str, language: str | None = None) -> str:
    lang = normalize_language(language)
    lang_map = MESSAGES.get(lang, MESSAGES["uk"])
    return lang_map.get(key, MESSAGES["uk"].get(key, key))
